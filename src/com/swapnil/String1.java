package src.com.swapnil;

class String1 {
    public static void main(String[] args) {
        String myString = "Swapnil bhojwani";
        System.out.println("Your string is " + myString);
        System.out.println("Your string length is " + myString.length());
        System.out.println("Your string in lower case is " + myString.toLowerCase());
        System.out.println("Your string in upper case is " + myString.toUpperCase());

    }
}